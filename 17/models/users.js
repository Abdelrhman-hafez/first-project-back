const mongoose = require('mongoose');
const { Product } = require('./product');

const userSchema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    phone: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    shoppingCart: {
        type:Array
    }
});

const users = mongoose.model('User', userSchema);

module.exports = { users };
